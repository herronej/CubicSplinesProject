#include <iostream>
#include <cmath>
using namespace std;

int main(){

    int n = 19;

    double x[n+1] = {8.1630459, 9.9877977, 13.264594, 15.497377, 25.080542, 25.721539, 37.991997, 40.501915, 49.486671, 54.893303, 58.295238, 63.289623, 65.739174, 70.579094, 71.344360, 80.231201, 82.764511, 94.691475, 94.734245, 102.93697};

    double a[n+1] = {50.828323, 55.781410, 64.178932, 69.237793, 79.999168, 79.933388, 61.068481, 54.471272, 30.977743, 20.959698, 17.896526, 19.237446, 22.158518, 29.073372, 29.915400, 25.415119, 20.121891, 2.0646029, 2.0406404, .13763143};


    //double x[n+1] = {9.9877977, 8.1630459, 13.264594, 15.497377, 25.080542, 25.721539, 37.991997, 40.501915, 49.486671, 54.893303, 58.295238, 63.289623, 65.739174, 70.579094, 71.344360, 82.764511, 80.231201, 94.734245, 94.691475, 102.93697};

    //double a[n+1] = {55.781410, 50.828323, 64.178932, 69.237793, 79.999168, 79.933388, 61.068481, 54.471272, 30.977743, 20.959698, 17.896526, 19.237446, 22.158518, 29.073372, 29.915400, 20.121891, 25.415119, 2.0406404, 2.0646029, .13763143};

    double h[n+1];
    double alpha[n+1];

    double l[n+1];
    double mu[n+1];
    double z[n+1];
    double b[n+1];
    double c[n+1];
    double d[n+1];    

    for(int i = 0; i < n; i++){
        h[i] = x[i+1] - x[i];
    }

    for(int i = 1; i < n; i++){
        alpha[i] = (3.0/h[i])*(a[i+1]-a[i]) - (3.0/h[i-1])*(a[i]-a[i-1]);
    }

    l[0] = 1.0;
    mu[0] = 0.0;
    z[0] = 0.0;

    for(int i = 1; i < n; i++){
        l[i] = 2.0*(x[i+1] - x[i-1])-h[i-1]*mu[i-1];
        mu[i] = h[i]/l[i];
        z[i] = (alpha[i] - h[i-1]*z[i-1])/l[i];
    }

    l[n] = 1.0;
    z[n] = 0.0;
    c[n] = 0.0;

    for(int j = n-1; j>= 0; j -= 1){
        c[j] = z[j] - mu[j]*c[j+1];
        b[j] = (a[j+1]-a[j])/h[j] - h[j]*(c[j+1]+2*c[j])/3.0;
        d[j] = (c[j+1]-c[j])/(3*h[j]);
    } 
 
    for(int j = 0; j < n; j++){
        cout << j << ": " << a[j] << "\t" << b[j] << "\t" << c[j] << "\t" << d[j] << endl;
    }

    double currX = x[0];
    int partIndex;    

    for(int i = 1; i <= 100; i++){
        
        for(int j = 0; j < n; j++){
            if(currX >= x[j] && currX <= x[j+1]){
                partIndex = j;
                //cout << "partIndex: " << partIndex << endl;
                break;
            }       
        }

        double total_y = a[partIndex] + b[partIndex]*(currX-x[partIndex]) + c[partIndex]*pow((currX-x[partIndex]),2.0) + d[partIndex]*pow((currX-x[partIndex]), 3.0);

        cout << currX << "\t" << total_y << endl;
                

        currX = currX + (x[n]-x[0])/100.0;
        
    }

}
